public class EineKlasse {
	
	/**
	 * Multipliziert zwei Werte
	 * @param a
	 * @param b
	 * @return Ergebnis
	 */
	public double multiplizieren(double a, double b) {
		return a * b;
	}
	
	/**
	 * Addiere zwei Werte
	 * @param a
	 * @param b
	 * @return Ergebnis
	 */
	public double addieren(double a, double b) {
		return a + b;
	}
	
	/**
	 * Dividiert zwei Werte
	 * @param a
	 * @param b
	 * @return Ergebnis
	 */
	public double dividieren(double a, double b) {
		return a / b;
	}

}
